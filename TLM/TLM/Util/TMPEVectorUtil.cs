namespace TrafficManager.Util {
    using UnityEngine;

    internal static class TMPEVectorUtil {
        internal static Vector3 RotateXZ90CW(this Vector3 v) =>
            new Vector3(v.z, v.y, -v.x);
        internal static Vector3 RotateXZ90CCW(this Vector3 v) =>
            new Vector3(-v.z, v.y, v.x);

    }
}
